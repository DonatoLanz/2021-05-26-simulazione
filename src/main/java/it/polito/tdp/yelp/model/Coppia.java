package it.polito.tdp.yelp.model;

public class Coppia {
 private String b1;
 private String b2;
 private double m1;
 private double m2;
public Coppia(String b1, String b2, double m1, double m2) {
	super();
	this.b1 = b1;
	this.b2 = b2;
	this.m1 = m1;
	this.m2 = m2;
}
public String getB1() {
	return b1;
}
public void setB1(String b1) {
	this.b1 = b1;
}
public String getB2() {
	return b2;
}
public void setB2(String b2) {
	this.b2 = b2;
}
public double getM1() {
	return m1;
}
public void setM1(double m1) {
	this.m1 = m1;
}
public double getM2() {
	return m2;
}
public void setM2(double m2) {
	this.m2 = m2;
}
 
 
 
}
